function drawStarfield(){
    ctx.drawImage(starfield, star_X, star_Y);
    ctx.drawImage(starfield, star_X2, star_Y);
    
    if(star_X < -600){
        star_X = 600;
    }
    if(star_X2 < -600){
        star_X2 = 600;
    }

    star_X -= 1;
    star_X2 -= 1;
}
