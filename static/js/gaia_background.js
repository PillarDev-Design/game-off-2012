function drawStarfield(){
    ctx.drawImage(starfield, star_X, star_Y);
    ctx.drawImage(starfield, star_X, star_Y2);
    
    if(star_Y > 600){
        star_Y = -599;
    }
    if(star_Y2 > 600){
        star_Y2 = -599;
    }

    star_Y += 1;
    star_Y2 += 1;
}
